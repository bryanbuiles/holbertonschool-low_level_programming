#include <stdlib.h>
#include <stdio.h>
#include "3-calc.h"

/**
 * op_add - sum operation
 * @a: number
 * @b: number
 * Return: sum
 */

int op_add(int a, int b)
{
return (a + b);
}

/**
 * op_sub - sub operation
 * @a: number
 * @b: number
 * Return: sub
 */

int op_sub(int a, int b)
{
return (a - b);
}

/**
 * op_mul - mul operation
 * @a: number
 * @b: number
 * Return: mul
 */

int op_mul(int a, int b)
{
return (a * b);
}

/**
 * op_div - mul operation
 * @a: number
 * @b: number
 * Return: div
 */

int op_div(int a, int b)
{
return (a / b);
}

/**
 * op_mod - mul operation
 * @a: number
 * @b: number
 * Return: mod
 */

int op_mod(int a, int b)
{
return (a % b);
}
